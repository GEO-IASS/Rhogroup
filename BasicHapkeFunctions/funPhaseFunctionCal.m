function p_g = funPhaseFunctionCal(b,c,g);
%%
%funPhaseFunctionCal.m
% This function calculates the phase function according to the double Heyney-
% Greenstein function
%----------------------------------------------------------------------------------------------------%
% INPUT
%----------------------------------------------------------------------------------------------------%
% b ,c,g        : - constants for the materials for the SSA
%----------------------------------------------------------------------------------------------------%
% OUTPUT
%----------------------------------------------------------------------------------------------------%
% p_g : - the phase Function
%----------------------------------------------------------------------------------------------------%
%
% Reference : - "Theory of Reflectance and Emittance Spectroscopy", Bruce 
%                           Hapke, ISBN: 9780521883498
%
%
% Author's Name : - Arun M Saranathan
% Date                  : - 01/23/2014
%----------------------------------------------------------------------------------------------------%
%%
mug = cosd(g);

p_g = ((1+c)/2) * ((1-b^2)/(1-(2*b*mug)+b^2)^(3/2)) + ...
    ((1-c)/2) * ((1-b^2)/(1+(2*b*mug)+b^2)^(3/2)) ;
return;